#ifndef _MODEL_H_
#define _MODEL_H_

#include <stdint.h>

/* Vertex */
struct vertex {
    float position[3];
    float normal[3];
    float tangent[3];
    float binormal[3];
    float color[4];
    float uvs[2];
};

/* Mesh */
struct mesh {
    int num_verts;
    int num_indices;
    struct vertex* vertices;
    uint32_t* indices;
    int mat_index;
};

/* Joint */
struct joint {
    struct joint* parent;
    float position[3];
    float rotation[4]; /* quat */
};

/* Skeleton */
struct skeleton {
    struct joint* joints;
    size_t num_joints;
};

/* Model */
struct model {
    int num_meshes;
    struct mesh** meshes;
    int num_materials;
    struct skeleton* skeleton;
};

struct model* model_new();
void model_delete(struct model*);

struct mesh* mesh_new();
void mesh_delete(struct mesh*);

struct skeleton* skeleton_new();
void skeleton_delete(struct skeleton*);
void skeleton_joint_transform(struct joint* j, float trans[16]);

#endif /* ! _MODEL_H_ */
